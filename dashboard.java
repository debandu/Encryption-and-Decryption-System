    private void refreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshActionPerformed
        if(LoginSession.isLoggedIn)
        {
        try{
            DefaultTableModel tb1Model=(DefaultTableModel)table.getModel();
            tb1Model.getDataVector().removeAllElements();
            Connection mycon=connection.getconnection();
            String query="select field,field_password from users where username='"+LoginSession.username+"' AND password='"+LoginSession.password+"'";
            Statement st=mycon.createStatement();
            ResultSet rs=st.executeQuery(query);
            while(rs.next())
            {
                String field=rs.getString("field");
                String pass=rs.getString("field_password");
                String tbData[]={field,pass};
                tb1Model.addRow(tbData);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e);
        }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"login in first");
            loginform rf=new loginform();
        rf.setVisible(true);
        rf.pack();
        rf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.dispose();
        }
    }//GEN-LAST:event_refreshActionPerformed

