    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        
        int ans=JOptionPane.showConfirmDialog(null,"ARE YOU SURE TO DELETE THE SELECTED DATA","DELETE",JOptionPane.YES_NO_OPTION);
        if(ans==0)
        {
        int row=table.getSelectedRow();
        String cell=table.getModel().getValueAt(row, 0).toString();
        
        try{
            Connection mycon=connection.getconnection();
            String query="delete from users where username='"+LoginSession.username+"' AND password='"+LoginSession.password+"' AND field='"+cell+"'";
            Statement st=mycon.createStatement();
            int rs=st.executeUpdate(query);
            JOptionPane.showMessageDialog(this,rs+" record deleted");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }
        }
    }//GEN-LAST:event_deleteActionPerformed

